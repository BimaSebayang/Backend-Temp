package id.co.buara.varia.computama.repository;

public class CartDataDetail {

	private String idItemDetail;
	private Integer banyakItemDiambil;
	private String idItem;
	
	public String getIdItemDetail() {
		return idItemDetail;
	}
	public void setIdItemDetail(String idItemDetail) {
		this.idItemDetail = idItemDetail;
	}
	public Integer getBanyakItemDiambil() {
		return banyakItemDiambil;
	}
	public void setBanyakItemDiambil(Integer banyakItemDiambil) {
		this.banyakItemDiambil = banyakItemDiambil;
	}
	public String getIdItem() {
		return idItem;
	}
	public void setIdItem(String idItem) {
		this.idItem = idItem;
	}
	
	
	
}
